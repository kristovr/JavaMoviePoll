package cinema;

public class Person {
    
    //--------ATTRIBUTES----------
    
    private String id;
    private String name;
    
    //--------ATTRIBUTES----------
    
    //--------CONSTRUCTOR----------

    public Person(String id, String name) {
        this.id = id;
        this.name = name;
    }
    
    //--------CONSTRUCTOR----------
    
    //--------GETTERS----------

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
    
    //--------GETTERS----------
    
    //--------SETTERS----------

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    //--------SETTERS----------
    
    
    
    
    
}
